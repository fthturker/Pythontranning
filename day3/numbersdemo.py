# find maximum of 3 numbers  max()
# max() işlevi ne döndürür, hangi sayının maksimum sayı olduğunu bulur
print(max(10,20,30,4,17,78,100,60,90)) # 100

# min() işlevi ne döndürür, hangi sayının minimum sayı olduğunu bulur
print(min(10,20,30,4,17,78,100,60,90)) # 4

